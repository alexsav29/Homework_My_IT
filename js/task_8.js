let x;

a = 8;
b = 3;

// a+2*(x-b)=16
// a + 2*x - 2*b = 16
x = (16 + 2 * b - a) / 2;
console.log("Результат первого уравнения, x =", x);

// b(x+15)=a+6*x;
// b*x + 15*b = a + 6*x
// b*x - 6*x = a - 15*b
// x*(b - 6) = a - 15*b
x = (a - 15 * b) / (b - 6);
console.log("Результат второго уравнения, x =", x);

// x+2*x+a*x+b*x=23780;
// x*(1 + 2 + a + b) = 23780;
x = 23780 / (1 + 2 + a + b);
console.log("Результат третьего уравнения, x =", x);
